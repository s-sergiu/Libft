#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	size_t			i;

	i = 0;
	while (i++ < n)
		*((unsigned char *)dst + i - 1) = *((unsigned char *)src + i - 1);
	return (dst);
}
